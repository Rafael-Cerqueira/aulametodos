package basecamp;

public class Calculadora {

    public static void soma(double a, double b){
        double soma = a + b;

        System.out.println("Soma: " + soma);
    }
    public static void subtração(double a, double b){
        double subtração = a - b;

        System.out.println("Sub: " + subtração);
    }
    public static void multiplicação(double a, double b){
        double multiplicação = a * b;

        System.out.println("Mult: " + multiplicação);
    }
    public static void divisão(double a, double b){
        double divisão = a / b;

        System.out.println("Div: " + divisão);
    }


}
